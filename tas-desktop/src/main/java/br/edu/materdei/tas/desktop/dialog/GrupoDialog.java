    private void setGrupo(JSONObject grupo) {
        this.grupo = grupo;
        tfNome.setText(grupo.getString("nome"));
    }

    @Override
    public void salvar() {
        this.setCursor(new Cursor(Cursor.WAIT_CURSOR));
        try {
            this.mainframe.setStatus("Salvando registro. Aguarde...");
            
            this.grupo.put("nome", tfNome.getText());
           
            try {
                BackendService.save("grupos", this.grupo);                
                this.mainframe.setStatus("Pronto!");
            } catch (Exception e) {
                this.fail = true;
                
                TasDialogError dlgError = new TasDialogError(mainframe);
                dlgError.showError(TasDialogError.DANGER_ERROR, 
                                   "<html>N√£o foi poss√≠vel salvar o registro. Clique em \"Detalhes\" Para visualizar o problema.</html>", 
                                   e.getMessage());
                this.mainframe.setStatus(new ImageIcon(getClass().getResource("/assets/system/icons/icon-warning-small.png")), 
                                             e.getMessage(), "Ops!");
                
            }
           
        } finally {
            this.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));
            dispose();
        }
    }

    @Override
    public void validarDados() throws NotValidateException {
        
        this.fieldsEmpty = new ArrayList<>();
        
        if (tfNome.getText().equals("")) {
            this.fieldsEmpty.add("O campo \"Nome\" deve ser preenchido.");
        }
        
        //Verifica se ocorreu erro
        if (this.fieldsEmpty.size() > 0)
            throw new NotValidateException("<html>Alguns campos obrigat√≥rios n√£o foram preenchidos! Corrija e tente novamente.</html>");
    }
